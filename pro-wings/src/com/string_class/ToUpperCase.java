package com.string_class;

public class ToUpperCase {
	public static void main(String[] args) {
		String s1=new String ("Vaibhav");
		String s2="Vaibhav";
		String s3="Khot ";
		
		System.out.println(s1.toUpperCase());
		System.out.println(s2.toUpperCase());
		
		s3=s3.toUpperCase();
		System.out.println(s3);
	}

}
